<h2>Etusivu</h2><br>

<h5>Yleistä</h5>
<p>
  Tämä verkkosivu on tehty harjoitustyönä kurssille IN00BP76 Internet ohjelmointi <br>
  ja relaatiotietokannat -osioihin. Sivu esittää yksinkertaistettua verkkopankkia.
</p>
<h5>Käyttöohjeet</h5>
<p>
  Tietoja-sivulla on listattuna verkkosivulla käytetyt tekniikat. Tunnukset-sivulla <br>
  on esiteltynä kaikki toimivat kirjautumistunnukset. Kirjautumalla sisään pääsee <br>
  kokeilemaan eri käyttäjien toiminnallisuutta.
</p>
<h5>Ominaisuuksia</h5>
<p>
  Pankkikortilla kirjautuessa salasana siirtyy salaamattomana tietokannasta. <br>
  URL-kenttään kirjoitettujen osoitteiden käyttöoikeus tarkistetaan, joten vierailija <br>
  ei voi suorittaa kirjautumista vaativia toimintoja.
</p>
<h5>Tekijät</h5>
<p>
  Harjoitustyön on tehnyt Arttu Tarkiainen.
</p>
